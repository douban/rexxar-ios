@interface RXRQueryStringPair : NSObject
@property (readwrite, nonatomic, strong) id field;
@property (readwrite, nonatomic, strong) id value;

- (instancetype)initWithField:(id)field value:(id)value;

- (NSString *)URLEncodedStringValue;
@end

@implementation RXRQueryStringPair

- (instancetype)initWithField:(id)field value:(id)value {
  self = [super init];
  if (!self) {
    return nil;
  }

  self.field = field;
  self.value = value;

  return self;
}

- (NSString *)URLEncodedStringValue {
  if (!self.value || [self.value isEqual:[NSNull null]]) {
    return RXRPercentEscapedStringFromString([self.field description]);
  } else {
    return [NSString stringWithFormat:@"%@=%@", RXRPercentEscapedStringFromString([self.field description]), RXRPercentEscapedStringFromString([self.value description])];
  }
}

@end

