static NSString * RXRCreateMultipartFormBoundary() {
  return [NSString stringWithFormat:@"Boundary+%08X%08X", arc4random(), arc4random()];
}

static NSString * const kRXRMultipartFormCRLF = @"\r\n";

static inline NSString * RXRMultipartFormInitialBoundary(NSString *boundary) {
  return [NSString stringWithFormat:@"--%@%@", boundary, kRXRMultipartFormCRLF];
}

static inline NSString * RXRMultipartFormEncapsulationBoundary(NSString *boundary) {
  return [NSString stringWithFormat:@"%@--%@%@", kRXRMultipartFormCRLF, boundary, kRXRMultipartFormCRLF];
}

static inline NSString * RXRMultipartFormFinalBoundary(NSString *boundary) {
  return [NSString stringWithFormat:@"%@--%@--%@", kRXRMultipartFormCRLF, boundary, kRXRMultipartFormCRLF];
}

static inline NSString * RXRContentTypeForPathExtension(NSString *extension) {
  NSString *UTI = (__bridge_transfer NSString *)UTTypeCreatePreferredIdentifierForTag(kUTTagClassFilenameExtension, (__bridge CFStringRef)extension, NULL);
  NSString *contentType = (__bridge_transfer NSString *)UTTypeCopyPreferredTagWithClass((__bridge CFStringRef)UTI, kUTTagClassMIMEType);
  if (!contentType) {
    return @"application/octet-stream";
  } else {
    return contentType;
  }
}

