FOUNDATION_EXPORT NSArray * RXRQueryStringPairsFromDictionary(NSDictionary *dictionary);
FOUNDATION_EXPORT NSArray * RXRQueryStringPairsFromKeyAndValue(NSString *key, id value);

NSString * RXRQueryStringFromParameters(NSDictionary *parameters) {
  NSMutableArray *mutablePairs = [NSMutableArray array];
  for (RXRQueryStringPair *pair in RXRQueryStringPairsFromDictionary(parameters)) {
    [mutablePairs addObject:[pair URLEncodedStringValue]];
  }

  return [mutablePairs componentsJoinedByString:@"&"];
}

NSArray * RXRQueryStringPairsFromDictionary(NSDictionary *dictionary) {
  return RXRQueryStringPairsFromKeyAndValue(nil, dictionary);
}

NSArray * RXRQueryStringPairsFromKeyAndValue(NSString *key, id value) {
  NSMutableArray *mutableQueryStringComponents = [NSMutableArray array];

  NSSortDescriptor *sortDescriptor = [NSSortDescriptor sortDescriptorWithKey:@"description" ascending:YES selector:@selector(compare:)];

  if ([value isKindOfClass:[NSDictionary class]]) {
    NSDictionary *dictionary = value;
    // Sort dictionary keys to ensure consistent ordering in query string, which is important when deserializing potentially ambiguous sequences, such as an array of dictionaries
    for (id nestedKey in [dictionary.allKeys sortedArrayUsingDescriptors:@[ sortDescriptor ]]) {
      id nestedValue = dictionary[nestedKey];
      if (nestedValue) {
        [mutableQueryStringComponents addObjectsFromArray:RXRQueryStringPairsFromKeyAndValue((key ? [NSString stringWithFormat:@"%@[%@]", key, nestedKey] : nestedKey), nestedValue)];
      }
    }
  } else if ([value isKindOfClass:[NSArray class]]) {
    NSArray *array = value;
    for (id nestedValue in array) {
      [mutableQueryStringComponents addObjectsFromArray:RXRQueryStringPairsFromKeyAndValue([NSString stringWithFormat:@"%@[]", key], nestedValue)];
    }
  } else if ([value isKindOfClass:[NSSet class]]) {
    NSSet *set = value;
    for (id obj in [set sortedArrayUsingDescriptors:@[ sortDescriptor ]]) {
      [mutableQueryStringComponents addObjectsFromArray:RXRQueryStringPairsFromKeyAndValue(key, obj)];
    }
  } else {
    [mutableQueryStringComponents addObject:[[RXRQueryStringPair alloc] initWithField:key value:value]];
  }

  return mutableQueryStringComponents;
}

